int DanceRound(void) {

    if (0 <= DSU && DSU < 5) {
        return 4;
    }
    else if (5 <= DSU && DSU < 10) {
        return 6;
    }
    else if (10 <= DSU && DSU < 15) {
        return 7;
    }
    else if (15 <= DSU && DSU < 20) {
        return 8;
    }
}
void DanceBreak(int Keyinput, int num) {

    int i, j, k;

    if (Keyinput == True) {
        for (i = 0; i <= 3; i++) {
            for (j = 0; j <= 3; j++) {
                if (DB[i][j + (5 * num)] >= 1)
                    DB[i][j + (5 * num)] = Void;
            }
        }
        DG += 1;
    }

    if (Keyinput == False)
    {
        for (k = 0; k < DanceRound(); k++) {
            switch (DS[k]) {
            case UP:
                for (i = 0; i <= 3; i++) {
                    for (j = 0; j <= 3; j++) {
                        DB[i][j + (5 * k)] = DanceBlocks[0][i][j];
                    }
                }
                for (i = 0; i <= 3; i++) {
                    for (j = 0; j <= 3; j++) {
                        if (DanceBlocks[0][i][j] >= 1)
                            DB[i][j + (5 * k)] = DanceBlocks[0][i][j];
                    }
                }
                break;
            case DOWN:
                for (i = 0; i <= 3; i++) {
                    for (j = 0; j <= 3; j++) {
                        if (DanceBlocks[1][i][j] >= 1)
                            DB[i][j + (5 * k)] = DanceBlocks[1][i][j];
                    }
                }
                break;
            case LEFT:
                for (i = 0; i <= 3; i++) {
                    for (j = 0; j <= 3; j++) {
                        if (DanceBlocks[2][i][j] >= 1)
                            DB[i][j + (5 * k)] = DanceBlocks[2][i][j];
                    }
                }
                break;
            case RIGHT:
                for (i = 0; i <= 3; i++) {
                    for (j = 0; j <= 3; j++) {
                        if (DanceBlocks[3][i][j] >= 1)
                            DB[i][j + (5 * k)] = DanceBlocks[3][i][j];
                    }
                }
                break;
            }
        }
        DG = 0;
    }
}
void DanceKeyinput(void) {

    keyinput = 0;

    if (_kbhit()) {

        keyinput = _getch();

        switch (keyinput) {
        case UP:
            if (DS[DG] == UP) {
                DanceBreak(True, DG);
            }
            else if (DS[DG] != UP) {
                DanceBreak(False, DG);
                timer -= 1;
                DF += 1;
            }
            break;
        case DOWN:
            if (DS[DG] == DOWN) {
                DanceBreak(True, DG);
            }
            else if (DS[DG] != DOWN) {
                DanceBreak(False, DG);
                timer -= 1;
                DF += 1;
            }
            break;
        case LEFT:
            if (DS[DG] == LEFT) {
                DanceBreak(True, DG);
            }
            else if (DS[DG] != LEFT) {
                DanceBreak(False, DG);
                timer -= 1;
                DF += 1;
            }
            break;
        case RIGHT:
            if (DS[DG] == RIGHT) {
                DanceBreak(True, DG);
            }
            else if (DS[DG] != RIGHT) {
                DanceBreak(False, DG);
                timer -= 1;
                DF += 1;
            }
            break;
        }
    }

    if (DG == DanceRound()) {
        ++DSU;
        DG = 0;
        DSUF = True;
    }
}
