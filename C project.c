void DanceOutput(void) {

    int i, j, k;

    D_X = 1;
    D_Y = 4;

    for (k = 0; k < DanceRound(); k++) {
        DT = rand() % 4;
        switch (DT) {
        case 0:
            DS[k] = UP;
            break;
        case 1:
            DS[k] = DOWN;
            break;
        case 2:
            DS[k] = LEFT;
            break;
        case 3:
            DS[k] = RIGHT;
            break;
        }

        for (i = 0; i <= 3; i++) {
            for (j = 0; j <= 3; j++) {
                if (DanceBlocks[DT][i][j] >= 1) {
                    DB[i][j + (5 * k)] = DanceBlocks[DT][i][j];
                }
            }
        }
    }
    DSUF = False;
}
