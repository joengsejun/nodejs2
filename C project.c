//버튼 움직임
void buttonmove() {
    int keyinput = 0;
    if (_kbhit()) {
        keyinput = _getch();
        switch (keyinput) {
        case UP:
            if (button2 == 1) {
                button2 = 0;
                button1 = 1;
            }
            else {
                button = 1;
                button1 = 0;
            }
            break;
        case DOWN:
            button = 0;
            if (button2 == 1) {
                button2 = 1;
            }
            else if (button1 == 1) {
                button1 = 0;
                button2 = 1;
                button = 0;
            }
            else {
                button1 = 1;
            }
            break;
        }
    }
}


